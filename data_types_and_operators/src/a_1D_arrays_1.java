
public class a_1D_arrays_1 {
	// create a 1d array
	public static void main(String[] args) {
		int[] array1 = new int[3];

		// value assignment
		array1[0] = 2;
		array1[1] = -10;
		array1[2] = 432;

		// print
		for (int i = 0; i < 3; i++) {
			System.out.println("The value of array [" + i + "] is " + array1[i]);
		}

		//

	}
}
